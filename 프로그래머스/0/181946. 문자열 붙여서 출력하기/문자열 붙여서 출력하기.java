import java.util.Scanner;
/*
두 개의 문자열 str1, str2가 공백으로 구분되어 입력으로 주어진다.
str1과 str2 이어서 출력
*/
public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String a = sc.next();
        String b = sc.next();
        System.out.println(a+b);
    }
}